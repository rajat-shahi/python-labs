"""
- we can use '+' to concatenate strings
- we can use '*' to repeat strings several amount of times
- we can use str() to typecast values to strings as per our need
"""

print("rajat" + " shahi")
print("ha" * 10)
print("ha" * 2)
print("ha" * 0)  # returns empty string
print("ha" * -5)  # returns empty string
